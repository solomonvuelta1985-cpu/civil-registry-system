<?php
/**
 * RA 9048 — Document Generator
 *
 * Auto-fills the office's .docx templates from a saved petition record and
 * writes the populated documents under uploads/ra9048/generated/petition_{id}/.
 *
 * Request (POST):
 *   petition_id   int           (required)
 *   doc_type      string        Optional. One of: petition, order_publication,
 *                                public_notice, cert_posting, cert_filing, all.
 *                                Defaults to 'all'.
 *
 * Response (JSON):
 *   {
 *     success: true,
 *     message: "...",
 *     data: {
 *       petition_id: 42,
 *       generated: [
 *         { doc_type: "petition", filename: "...", url: "...", template: "..." },
 *         ...
 *       ],
 *       skipped: [
 *         { doc_type: "order_publication", reason: "Subtype CCE_minor doesn't require publication." }
 *       ]
 *     }
 *   }
 *
 * Each output document is generated by:
 *   1. Loading the matching .docx template from documents/templates/
 *   2. Running it through DocxTemplateProcessor with the petition's data + child rows
 *   3. Saving the populated .docx under uploads/ra9048/generated/petition_{id}/
 *
 * Templates are expected to use ${placeholder} tokens. Phase 4b is the manual
 * step where the user opens each template in Word and replaces hardcoded
 * sample data with the placeholders listed in the cheat sheet.
 */

require_once '../../includes/config_ra9048.php';
require_once '../../includes/functions.php';
require_once '../../includes/auth.php';
require_once '../../includes/security.php';
require_once __DIR__ . '/../../includes/DocxTemplateProcessor.php';

header('Content-Type: application/json');

requireAuth();
requireCSRFToken();

if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    json_response(false, 'Invalid request method.', null, 405);
}

// ---- Constants ----------------------------------------------------------

const RA9048_GENERATED_SUBDIR = 'generated';

/**
 * Map doc_type → template filename + output filename pattern + applies-to logic.
 * Output filenames are intentionally simple, no spaces, ASCII only.
 */
function ra9048_doc_definitions(): array
{
    return [
        // Petition itself — chooses CFN or CCE template based on subtype.
        // Uses the ORIGINAL office templates that the user manually placeholder-edits
        // in Word (find/replace sample data with ${...} tokens). The system fills
        // those placeholders at generation time.
        'petition' => [
            'label'       => 'Petition',
            'output_stem' => 'petition',
            'template_for_subtype' => [
                'CCE_minor' => 'RA 9048 petition (birth) 2.docx',
                'CCE_10172' => 'RA 9048 petition (birth) 2.docx',
                'CFN'       => 'RA 9048 (CFN) 2.docx',
            ],
            'applies_to' => ['CCE_minor', 'CCE_10172', 'CFN'],
        ],
        'order_publication' => [
            'label'       => 'Order for Publication',
            'output_stem' => 'order_for_publication',
            'template'    => 'publication(CFN).docx',
            'applies_to'  => ['CCE_10172', 'CFN'],
        ],
        'public_notice' => [
            'label'       => 'Public Notice',
            'output_stem' => 'public_notice',
            'template'    => 'RA 10172 publication.pptx',
            'applies_to'  => ['CCE_10172', 'CFN'],
            'note'        => 'pptx — handled separately if/when needed; same placeholder approach.',
        ],
        'cert_posting' => [
            'label'       => 'Certificate of Posting',
            'output_stem' => 'cert_of_posting',
            'template'    => 'cert of posting.doc',
            'applies_to'  => ['CCE_minor', 'CCE_10172', 'CFN'],
        ],
        'cert_filing' => [
            'label'       => 'Certification of Proof of Filing',
            'output_stem' => 'certification_of_filing',
            'template'    => 'certification.doc',
            'applies_to'  => ['CCE_minor', 'CCE_10172', 'CFN'],
        ],
    ];
}

// ---- Inputs -------------------------------------------------------------

$petition_id = (int) ($_POST['petition_id'] ?? 0);
$doc_type    = sanitize_input($_POST['doc_type'] ?? 'all');

if ($petition_id <= 0) {
    json_response(false, 'petition_id is required.', null, 400);
}

$defs = ra9048_doc_definitions();
$validTypes = array_keys($defs);
if ($doc_type !== 'all' && !in_array($doc_type, $validTypes, true)) {
    json_response(false, 'Unknown doc_type.', null, 400);
}

// ---- Load petition + child rows -----------------------------------------

try {
    $stmt = $pdo_ra->prepare("SELECT * FROM petitions WHERE id = :id AND status = 'Active'");
    $stmt->execute([':id' => $petition_id]);
    $petition = $stmt->fetch();
    if (!$petition) {
        json_response(false, 'Petition not found.', null, 404);
    }

    $stmt = $pdo_ra->prepare(
        "SELECT * FROM petition_corrections WHERE petition_id = :id ORDER BY item_no ASC"
    );
    $stmt->execute([':id' => $petition_id]);
    $corrections = $stmt->fetchAll();

    $stmt = $pdo_ra->prepare(
        "SELECT * FROM petition_supporting_docs WHERE petition_id = :id ORDER BY item_no ASC"
    );
    $stmt->execute([':id' => $petition_id]);
    $supporting_docs = $stmt->fetchAll();

} catch (PDOException $e) {
    error_log('RA9048 generate_document load error: ' . $e->getMessage());
    json_response(false, 'Failed to load petition.', null, 500);
}

// ---- Build value bag (every placeholder a template might reference) -----

$values = ra9048_build_template_values($petition, $corrections, $supporting_docs);

// ---- Decide which doc_types to generate ---------------------------------

$subtype  = $petition['petition_subtype'] ?? '';
$toGenerate = ($doc_type === 'all') ? $validTypes : [$doc_type];

$outputDir = RA9048_UPLOAD_PATH . RA9048_GENERATED_SUBDIR . '/petition_' . $petition_id . '/';
if (!is_dir($outputDir) && !mkdir($outputDir, 0755, true) && !is_dir($outputDir)) {
    json_response(false, 'Failed to create output directory.', null, 500);
}

$generated = [];
$skipped   = [];

foreach ($toGenerate as $type) {
    $def = $defs[$type];

    if (!in_array($subtype, $def['applies_to'], true)) {
        $skipped[] = [
            'doc_type' => $type,
            'reason'   => "Subtype {$subtype} does not require {$def['label']}.",
        ];
        continue;
    }

    // Resolve the template path
    if (isset($def['template_for_subtype'])) {
        $tplName = $def['template_for_subtype'][$subtype] ?? null;
    } else {
        $tplName = $def['template'] ?? null;
    }
    if (!$tplName) {
        $skipped[] = ['doc_type' => $type, 'reason' => 'No template configured.'];
        continue;
    }

    $tplPath = RA9048_TEMPLATES_PATH . $tplName;
    if (!is_file($tplPath)) {
        $skipped[] = ['doc_type' => $type, 'reason' => "Template file missing: {$tplName}"];
        continue;
    }

    $ext = strtolower(pathinfo($tplName, PATHINFO_EXTENSION));

    // .doc and .pptx fallthrough — DocxTemplateProcessor only handles .docx.
    // For Phase 4a we still record the intent; Phase 4b can convert these
    // templates to .docx so the same engine processes them.
    if ($ext !== 'docx') {
        $skipped[] = [
            'doc_type' => $type,
            'reason'   => "Template format ({$ext}) not yet supported by the engine — convert {$tplName} to .docx in Phase 4b.",
        ];
        continue;
    }

    $outputName = $def['output_stem'] . '_' . $petition_id . '.docx';
    $outputPath = $outputDir . $outputName;

    try {
        $processor = new DocxTemplateProcessor($tplPath);
        $processor->setValues($values['scalar']);

        // Table-row clones for grids — only fire if the template has the marker.
        $processor->cloneRowAndSetValues('corrections_block',     $values['corrections_rows']);
        $processor->cloneRowAndSetValues('supporting_block',      $values['supporting_rows']);
        $processor->cloneRowAndSetValues('public_notice_block',   $values['public_notice_rows']);

        $processor->saveAs($outputPath);

        // Auto-convert .docx → .pdf via LibreOffice headless. The PDF lives next
        // to the .docx so the preview modal and "Download PDF" button can grab it.
        // If conversion fails (LibreOffice missing on the host, etc.), the .docx
        // is still usable — we just record pdf_url as null.
        $pdfName = $def['output_stem'] . '_' . $petition_id . '.pdf';
        $pdfPath = $outputDir . $pdfName;
        $pdfUrl  = null;
        $pdfError = ra9048_convert_to_pdf($outputPath, $outputDir);
        if ($pdfError === null && is_file($pdfPath)) {
            $pdfRelPath = 'ra9048/' . RA9048_GENERATED_SUBDIR . '/petition_' . $petition_id . '/' . $pdfName;
            $pdfUrl = rtrim(defined('BASE_URL') ? BASE_URL : '/iscan/', '/')
                    . '/api/serve_ra9048_doc.php?file=' . rawurlencode($pdfRelPath);
        } elseif ($pdfError !== null) {
            error_log("RA9048 PDF conversion failed for {$outputName}: {$pdfError}");
        }

        // URL is served via api/serve_ra9048_doc.php?file=...
        $relPath  = 'ra9048/' . RA9048_GENERATED_SUBDIR . '/petition_' . $petition_id . '/' . $outputName;
        $baseUrl  = rtrim(defined('BASE_URL') ? BASE_URL : '/iscan/', '/');
        $generated[] = [
            'doc_type' => $type,
            'label'    => $def['label'],
            'filename' => $outputName,
            'url'      => $baseUrl . '/api/serve_ra9048_doc.php?file=' . rawurlencode($relPath),
            'pdf_url'  => $pdfUrl,
            'template' => $tplName,
        ];
    } catch (Throwable $e) {
        error_log("RA9048 generate_document [{$type}] error: " . $e->getMessage());
        $skipped[] = [
            'doc_type' => $type,
            'reason'   => 'Generation failed: ' . $e->getMessage(),
        ];
    }
}

// ---- Activity log + response -------------------------------------------

if (!empty($generated)) {
    $labels = array_map(function ($g) { return $g['label']; }, $generated);
    log_activity(
        $pdo,
        'RA9048 Documents Generated',
        "Petition #{$petition_id} ({$petition['petition_number']}): " . implode(', ', $labels),
        $_SESSION['user_id'] ?? null
    );
}

$msgGen  = count($generated);
$msgSkip = count($skipped);
$message = $msgGen
    ? "Generated {$msgGen} document(s)" . ($msgSkip ? ", skipped {$msgSkip}." : '.')
    : ($msgSkip ? "No documents generated. {$msgSkip} skipped." : 'Nothing to do.');

json_response(true, $message, [
    'petition_id' => $petition_id,
    'generated'   => $generated,
    'skipped'     => $skipped,
]);


